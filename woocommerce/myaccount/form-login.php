 * Sur mobile : empilées.
 */

if ( ! defined( 'ABSPATH' ) ) {
	exit;
}

do_action( 'woocommerce_before_customer_login_form' );

$registration_enabled = 'yes' === get_option( 'woocommerce_enable_myaccount_registration' );
?>

<div class="dm-auth-overlap">
    <div class="dm-auth-overlap__inner">

        <?php if ( $registration_enabled ) : ?>
        <div class="dm-auth-cards">
            <div class="dm-auth-card dm-auth-card--login">
        <?php endif; ?>

                <div class="dm-auth-card__header">
                    <div class="dm-auth-card__icon">
                        <svg xmlns="http://www.w3.org/2000/svg" width="24" height="24" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
                            <path d="M15 3h4a2 2 0 0 1 2 2v14a2 2 0 0 1-2 2h-4"/>
                            <polyline points="10 17 15 12 10 7"/>
                            <line x1="15" y1="12" x2="3" y2="12"/>
                        </svg>
                    </div>
                    <h2 class="dm-auth-card__title"><?php esc_html_e( 'Connexion', 'astra-delices-de-la-mer' ); ?></h2>
                </div>

                <form class="woocommerce-form woocommerce-form-login login" method="post" novalidate>

                    <?php do_action( 'woocommerce_login_form_start' ); ?>

